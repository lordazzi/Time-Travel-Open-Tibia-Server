				<p>As Amazonas são uma vocação muito peculiar, elas começam como guerreiras quando são jovens adolescentes, tendo maior
				foco na capacidade do uso armas, quando uma Amazona cresce e amadurece, ela se torna uma Valquiria <strong><em>(Valkyrie)</em></strong>,
				elas tem um equilibrio entre controle de magia e uso de armas. Quando uma Valquiria se torna uma Bruxa <strong><em>(Witch)</em></strong>,
				ela normalmente já está muito velha e para o treinamento com armas para focar no treinamento com Magias.</p>
				<p>Depois que as Amazonas foram para a cidade e se depararam com o sistema de vocações e o copiaram, criaram ainda o Conselho de Amazonas
				que, assim como os outros Conselho, é responsável por dizer quando uma Amazona chegou ao nível de Valquiria ou de Bruxa.</p>
				
				<h3>Observações</h3>
				<p>Apenas personagens femininos podem ser Amazonas</p>
