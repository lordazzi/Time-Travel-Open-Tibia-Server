			<h2><strong>Dwarfian (Anões)</strong></h2>
			<div class="body">
			
				<a href="#mage">Mago</a> | <a href="#tribal">Tribal</a> | <a href="#elfian">Elfiano</a> |
				<a href="#wild">Selvagem</a> | <a href="#ninja">Ninja</a> | <a href="#amazon">Amazona</a> |
				<a href="#dwarfian">Anão</a> | <a href="#monk">Monge</a> | <a href="#lord">Senhor</a>
			
				<h3>História</h3>
				<p>Da mesma forma que os Elfos, os Anões são uma etnia. Hoje é dificil achar um Anão puro sangue, isso ocorre por que os Anões nunca
				se preocuparam em manter a pureza da etinia, mas sim em expandir o conhecimento que eles tem em machados.</p>
				<p>Os Anões sempre disseram que o uso da Magia é totalmente dispensável, graças a esse tipo de pensamento, oitossentos anos depois da
				fundação do Conselho da Magia (que antes ignorava os Anões pelo fato deles não terem poderes magicos), resolveram extermina-los.</p>
				<p>A guerra contra os Anões chegou a gerar uma esperança para as pessoas que desejavam logo que o Conselho da Magia caísse, um duelo
				clássico entre força fisica e força magica.</p>
				<p>Os anões são lentos e atacam de perto, os magos não são os mais rapidos de todos, mas conseguiam manter uma certa distância para
				usar magias, se um Anão conseguia se aproximar de um Mago o suficiente para ataca-lo, era morte certa para o Mago.</p>
				<p>Como quando os Magos e os Anões se apresentavam no campo de batalha para se enfrentarem os Anões tinham grande perda por causa
				da desvantagem da distância, os Anões bolaram uma estratégia contra os Magos, dividiram o exército em duas partes, uma que
				ficaria distante e outro que ficou no subsolo raso e só sairam na hora certa.</p>
				<p>Muitos Magos começaram a morrer para os Anões, até que os Magos planejaram um ataque surpresa na base dos Anões, entrando pelo subsolo
				e atacando a cidade dos Anões no subsolo, o grande erro do plano era que os Anões utilizavam energia baseada em gás e quando os Magos
				invadiram a cidade soltando bolas de fogo todo o subsolo explodiu matando tanto os Magos como os Anões.</p>
				<p>Foi uma grande tragédia que deixou o Império do Conselho da Magia muito afetado, varios Magos perderam muitos parentes nessa época
				e isso fez com que uma luz brilhasse no fim do tunel: o Conselho de Magia resolveu abandonar o Império e chamou os Anões sobreviventes para
				fazer um tratado de paz. Mas tudo na verdade era uma cilada e os Anões foram congelados e aprisionados na caideia do subsolo.</p>
				<p>Sem as suas picaretas não havia como os Anões cavarem tuneis dentro da cadeia, pelo menos era assim que os Magos pensavam, depois que os
				Anões se recuperarem do grande trauma que sofreram pela morte de quase todas as pessoas de seu povo, os Anões começaram a cavar um tunel
				em direção a cidade usando as mãos.</p>
				<p>O tunel foi concluido seissentos e cinquenta anos depois que os anões foram presos, mas apenas Anões conseguiam passar por ele e na
				hora da fuga, eles resolveram ficar morando por lá mesmo, depois de tanto tempo que já viviam naquele local e com isso o tunel ficou
				abandonado até o tempo em que os Selvagens chegaram na cadeia.</p>
				
				<h3>Habilidades</h3>
				<ul>
					<li>Não tem nenhuma habilidade magica.</li>
					<li>Não gostam de usar armas que não sejam machados, em machados eles são especialistas e não há ninguém que seja capaz de usar
					um machado como um Anão.</li>
					<li>Tem uma ótima defesa.</li>
					<li>Tem um capacidade de carregar muitas coisas apesar do tamanho.</li>
					<li>Tem a maior quantidade de vida de todos.</li>
					<li>São os piores pescadores de todos.</li>
				</ul>
				
				<h3>Promoção</h3>
				<p>Os Anões tem três classes que dividem os Anões: os Anões normais, que apesar de serem poderosos como todo Anão eles eram mais mantidos
				para a contrução de minas, extração de minérios, iscas de guerra. Os Anões Soldado <strong><em>(Soldier Dwarf)</em></strong> que eram os 
				Anões que já tinham criado tantos músculos nas minas que começavam a serem usados como guardas, soldados etc. E por fim, os soldados que
				se destacavam entre os outros soldados, ganhavam um título de reconhecimento pela sua força, o título de Anão Guerreiro. <strong><em>(Dwarf
				Warrior)</em></strong>, ninguém gostaria de cruzar o caminho de um Anão Guerreiro em seu momento de raiva.</p>
				<p>Depois da Guerra da Libertação, os Anões sairam da cadeia e começaram a morar na cidade e adaptaram o sistema de vocações antigo ao
				sistema clássico de todas as vocações: para se tornar um Dwarf Soldado é necessario ser capaz e também doar uma pequena quantia para os
				Anões líderes.</p>
				
				<h3>Observações</h3>
				<p>Apenas personagens masculinos podem ser Anões.</p>
