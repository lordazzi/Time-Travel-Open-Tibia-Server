			<h2><strong>Monk (Monges)</strong></h2>
			<div class="body">
			
				<a href="#mage">Mago</a> | <a href="#tribal">Tribal</a> | <a href="#elfian">Elfiano</a> |
				<a href="#wild">Selvagem</a> | <a href="#ninja">Ninja</a> | <a href="#amazon">Amazona</a> |
				<a href="#dwarfian">Anão</a> | <a href="#monk">Monge</a> | <a href="#lord">Senhor</a>
			
				<h3>História</h3>
				<p>Os Monges foram formados depois de cem anos que o Império do Conselho da Magia iniciou, eram Magos que acharam que as coisas que o Conselho
				estava fazendo eram injustas e sem sentido e então deixaram de serem Magos e criaram uma nova classe de uso de magia: os Monges, ou Monges
				Urbanos.</p>
				<p>Os Monges até que foram bem aceitos pelo Conselho de Magia, considerando que eles não tinham objetivo de usar magia para guerra ou defesa
				pessoal, mas sim para curar feridos de guerra. Apesar da aceitação, existiam algumas exigencias que o Conselho da Magia fez que os Monges 
				deveriam cimprir, se alguém decide ser um Monge deve abandonar tudo o que se refere a ser um Mago: não devem existir meio Monges meio Magos,
				os Monges devem ter um sistema de vocações com a mesma ideia do estilo de vocações dos Magos, precisando ter a necessidade de pagar uma pequena
				taxa para o Conselho da Magia para que o Monge possa ser promovido de uma vocação para outra.</p>
				<p>Os Monges de inicio não tinham nenhum tipo de magia de ataque, mas com o tempo foi permitido que eles aprendesse alguma coisa relacionada
				a ataque desde que essa magia fosse relacionada com a manipulação de luz.</p>
				<p>Os Monges se dedicaram sempre aos estudos e com isso eles contribuiram muito com a ciência, não só ao que se refere ao estudo da magia,
				mas também no estudo da natureza não magica.</p>
				<p>Mil e setessentos e trinta anos depois no inicio da fundação dos Monges eles descobriram a aplicação da eletricidade para armazenar e processar
				informações, criando assim, o primeiro computador.</p>
				<p>Cem anos depois da criação de computadores, os Monges começaram a ter mais facilidade em fazer cálculos e pesquisas sobre a natureza e guardar
				todas essas informações durante os séculos, estudos sobre biologia, física, matematica, magia e de todos os tipos, tudo bem organizado.</p>
				<p>Os computadores dos Monges eram guardados no Forte-Monastério no Norte do continente, tudo sempre foi muito bem protegido, mas nunca houve
				registros de alguém tentando destruir o Forte.</p>
				<p>Vinte anos depois do fim da Guerra da Libertação os Magos reformaram o Monastério e fundaram a <strong>Universidade de Tecnologia Magica</strong>,
				destribuindo as informações que eles armazenaram pelos séculos com todos os que desejassem aprender.</p>
				<p>A UTM durou duzentos e dez anos, isso graças a um projeto iniciado por Tai Kan Shin, um Ninja que deseja viver perpetuamente, junto de sua
				equipe ele analisou a magia, a biologia, a quimica, varias das infomações conquistadas pelos Monges durante os Milenios, fez varios testes e
				estudos durante sua vida toda, Tai Kan Shin morreu sem conseguir terminar o projeto, mas seus estudos foram continuados e um grupo de Monges, Ninjas
				e Elfos continuaram as pesquisas de sua equipe. Num belo dia de estudos da faculdade, houve um acidente no labotatório que liberou um gás que
				estava sendo testado por esta equipe, matando todos os alunos da Universidade.</p>
				<p>Todas as pessoas ficaram muito abaladas com as coisas que haviam acontecido na UTM e alguns dos Magos foram enviados pelo conselho de Magia 
				para conter o vasamento de gás mortal, quando chegaram lá, todos os mortos no incidente estavam vivos, só que débeis, fedendo morte, altamente
				agressivos e com uma força descomunal, três dos Magos enviados foram mortos pelos monstros. O Conselho de Magia solicitou aos Tribais que
				selassem a Universidade cobrindo-a com plantas para que ninguém mais fosse para lá, desde então ela está abandonada.</p>
				<p>A maior parte dos Monges são respeitadores da vida e buscam sempre fazer o que é direiro, apenas pelo fato de ser direito. Alguns deles
				são vegetarianos e alguns poucos chegam a comer apenas coisas que já estão num certo nível de decomposição.</p>
				
				<h3>Habilidades</h3>
				<ul>
					<li>Tem basicamente magias de cura, de todos os tipos.</li>
					<li>Tem poucas magias de ataque, mas tem.</li>
					<li>O uso excessivo de magias da luz fizeram com que os Monges tivessem uma grande quantidade de vitalidade.</li>
					<li>São os que tem maior concentração de energia magica.</li>
					<li>Sabem pescar, apesar de que só alguns pescam para comer.</li>
				</ul>
				
				<h3>Promoção</h3>
				<p>Da mesma forma que os Magos, os Monges também tem três níveis vocacionais, um inicial e duas promoções. Os títulos são Clero <strong><em>(Cleric)
				</em></strong> e Iluminado <strong><em>(Illuminated)</em></strong>, para receber esses títulos, o Monge deve ser capacidatado e fazer uma pequena
				contribuição com o Conselho de Magia.</p>
				
				<h3>Legado</h3>
				<p>A evolução das magias de cura e avanço técnologico e tudo devido aos Monges.</p>
				
				<h3>Mito</h3>
				<p>Rege a lenda que enquanto a Universidade de Tecnologia e Magia existiu, um grupo de Monges iniciaram um projeto de desenvolver um super computador
				que seria capaz de dividir por zero, depois de muitas frutrações eles conseguiram desenvolver. Quando o computador foi ligado e fez a primeira 
				divisão por zero, ele implodiu e abriu um vortex que atravessa o tempo e o espaço emitindo energia radioativa mortal que matou todos os presentes.</p>
				<p>Dizem que o vortex está até hoje na UTM em uma sala de difícil acesso.</p>
